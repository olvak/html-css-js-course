function phrase3words(value1, value2, value3) {
    let answer = (value1 + " " + value2 + " " + value3);  
    return answer;
}
let result1 = phrase3words("smile","at","everyone");
print(result1);
let result2 = phrase3words("everyone","wave", "back");
print(result2);
let result3 = phrase3words("coding","is", "fun");
print(result3);